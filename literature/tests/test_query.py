def test_related_no_edges(
    tmp_path: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    """related for a paper with no edges produces empty output."""
    # Add a 4th isolated paper to the index
    root = _setup_literature_dir(tmp_path)

    isolated_md = root / "papers" / "isolated2023alone.md"
    isolated_md.write_text(
        "---\ndoc_id: isolated2023alone\ntitle: Isolated Paper\nyear: 2023\n"
        "reading_status:\n  global: unread\nresource_type: paper\ntags: []\n---\n",
        encoding="utf-8",
    )

    # Patch graph.yaml to include the isolated node (no edges for it)
    y = YAML()
    graph_path = root / "index" / "graph.yaml"
    graph = y.load(graph_path.read_text(encoding="utf-8"))
    graph["nodes"]["isolated2023alone"] = {
        "title": "Isolated Paper",
        "year": 2023,
        "resource_type": "paper",
        "tags": [],
    }
    stream = __import__("io").StringIO()
    y.dump(graph, stream)
    graph_path.write_text(stream.getvalue(), encoding="utf-8")

    exit_code = run(["related", "isolated2023alone"], root=root)

    captured = capsys.readouterr()
    assert exit_code == 0
    assert captured.out.strip() == ""


# ── similar tests ──────────────────────────────────────────────────────────


def test_similar_returns_ranked_results(
    tmp_path: Path, capsys: pytest.CaptureFixture[str]
) -> None:
    """similar subcommand returns papers ranked by cosine similarity."""
    # Setup: create papers dir + embeddings.yaml
    papers_dir = tmp_path / "papers"
    papers_dir.mkdir()
    index_dir = tmp_path / "index"
    index_dir.mkdir()

    # Create query paper
    query_paper = papers_dir / "query_paper.md"
    query_paper.write_text(
        "---\ndoc_id: query_paper\ntitle: Query Paper\nyear: 2023\n"
        "reading_status:\n  global: unread\nresource_type: paper\ntags: []\n---\n",
        encoding="utf-8",
    )

    similar_paper = papers_dir / "similar_paper.md"
    similar_paper.write_text(
        "---\ndoc_id: similar_paper\ntitle: Similar Paper\nyear: 2023\n"
        "reading_status:\n  global: unread\nresource_type: paper\ntags: []\n---\n",
        encoding="utf-8",
    )

    dissimilar_paper = papers_dir / "dissimilar_paper.md"
    dissimilar_paper.write_text(
        "---\ndoc_id: dissimilar_paper\ntitle: Dissimilar Paper\nyear: 2023\n"
        "reading_status:\n  global: unread\nresource_type: paper\ntags: []\n---\n",
        encoding="utf-8",
    )

    # Create embeddings.yaml
    y = YAML()
    embeddings_data = {
        "model": "specter_v2",
        "dimensions": 3,
        "vectors": {
            "query_paper": [1.0, 0.0, 0.0],
            "similar_paper": [0.99, 0.1, 0.0],  # very similar
            "dissimilar_paper": [0.0, 0.0, 1.0],  # dissimilar
        },
    }
    with (index_dir / "embeddings.yaml").open("w") as f:
        y.dump(embeddings_data, f)

    exit_code = run(["similar", "query_paper", "--top", "1"], root=tmp_path)
    captured = capsys.readouterr()

    assert exit_code == 0
    assert "similar_paper" in captured.out
    # dissimilar_paper should not appear (only top 1)
    assert "dissimilar_paper" not in captured.out


