# ── Helpers ────────────────────────────────────────────────────────────────────


def _setup_literature_dir(tmp_path: Path) -> Path:
    """Create a minimal literature directory with 3 papers and pre-built index."""
    root = tmp_path / "literature"
    papers_dir = root / "papers"
    papers_dir.mkdir(parents=True)
    index_dir = root / "index"
    index_dir.mkdir()

    # Write paper files
    (papers_dir / "alpha2020method.md").write_text(PAPER_ALPHA, encoding="utf-8")
    (papers_dir / "beta2021bert.md").write_text(PAPER_BETA, encoding="utf-8")
    (papers_dir / "gamma2022diffusion.md").write_text(PAPER_GAMMA, encoding="utf-8")

    # Write pre-built index files
    (index_dir / "graph.yaml").write_text(GRAPH_YAML, encoding="utf-8")
    (index_dir / "status.yaml").write_text(STATUS_YAML, encoding="utf-8")

    return root


